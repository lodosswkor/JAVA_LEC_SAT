public class VariableLec04 {

	public static void main(String args[]) {
		
		char alphaNum = 65; //-- 대문자 A 
		char alpha = 'A' + 10; //-- 문자를 표현할때는 싱클쿼테이션 '' 
		                  //-- "" <-- 문자열에만 사용 
		
		System.out.println(alphaNum); // A 출력  
		System.out.println(alpha);  // A 출력 
		
		
		
	}
	
}
